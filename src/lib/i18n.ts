type Translations = typeof translations.ja;
type Section = keyof Translations;

export function t<S extends Section>(
  section: S,
  key: keyof Translations[S],
  lang: Lang
): string {
  const dict = translations[lang] as unknown as Translations;
  return (dict[section] as Record<string, unknown>)[key as string] as string;
}

export function tArr<S extends Section>(
  section: S,
  key: keyof Translations[S],
  lang: Lang
): string[] {
  const dict = translations[lang] as unknown as Translations;
  return (dict[section] as Record<string, unknown>)[key as string] as string[];
}

export function getReplanSuggestions(lang: Lang): string[] {
  return translations[lang].replanSuggestions as unknown as string[];
}
